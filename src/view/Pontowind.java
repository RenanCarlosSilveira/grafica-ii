    private void bAddPontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddPontoActionPerformed
        if (!nomePonto.getText().equals("")) {
            if (!x1Ponto1.getText().equals("") && !y1Ponto.getText().equals("")) {
                Ponto ponto = new Ponto(Integer.parseInt(x1Ponto1.getText()), Integer.parseInt(y1Ponto.getText()));
                ponto.setNome(nomePonto.getText());
                DesenhaObjetos.pontos.add(ponto);
                DesenhaObjetos.desenhaObjetos((Graphics2D) NMain.painelPintura.getGraphics());
                System.out.println("Adicionou ponto");
                dispose();
            } else {
                JOptionPane.showConfirmDialog(null, "Favor preencher os campos!", "", JOptionPane.PLAIN_MESSAGE);
            }
        } else {
            JOptionPane.showConfirmDialog(null, "DÃª um nome ao objeto", "", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_bAddPontoActionPerformed

